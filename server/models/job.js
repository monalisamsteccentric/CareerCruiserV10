import { Schema, model } from "mongoose";

// Define schema for job postings
const jobSchema = new Schema({
  title: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String,
    required: true,
    trim: true
  },
  location: {
    type: String,
    required: true,
    trim: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

// Create model from schema
const Job = model('CreatedJob', jobSchema);

export default Job
